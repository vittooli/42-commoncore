#include "libft.h"

int	ft_isalnum(int a)
{
	if ((48 <= a) && (a <= 57))
	{
		return (1);
	}
	if (((65 <= a) && (a <= 90)) || ((97 <= a) && (a <= 122)))
	{
		return (1);
	}
	return (0);
}
